package com.gcs.sysmgr.vo;


/**
 * formatter 返回参VO
 * @author yh
 *
 */
public class GCSFormatterVO {
	
	private String formatterId;
	
	private String formatterValue;
	
	private String formatterHiddenValue;

	public String getFormatterId() {
		return formatterId;
	}

	public void setFormatterId(String formatterId) {
		this.formatterId = formatterId;
	}

	public String getFormatterValue() {
		return formatterValue;
	}

	public void setFormatterValue(String formatterValue) {
		this.formatterValue = formatterValue;
	}

	public String getFormatterHiddenValue() {
		return formatterHiddenValue;
	}

	public void setFormatterHiddenValue(String formatterHiddenValue) {
		this.formatterHiddenValue = formatterHiddenValue;
	}
	
	

}
